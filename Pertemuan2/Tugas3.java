public class Tugas3 {

    public static void main(String[] args) {

        System.out.print("Nama Buah : ");

        /* \n digunakan untuk memberi jarak satu baris */
        System.out.print("\n1. Manggis");

        /* \t digunakan untuk memberi jarak beberapa spasi seperti tab */
        System.out.print("\t2. Semangka");

        /* fungsi \b adalah untuk backspace yaitu menghapus satu huruf dibelakangnya */
        System.out.print("\b3. Durian");
        System.out.print("4. Salak");
    }
}