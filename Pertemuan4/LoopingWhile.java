public class LoopingWhile {
    public static void main(String[] args) {
        
        System.out.println("Output Vertikal");
        int i = 10;
        while ( i <= 20 ){
        System.out.println(i);
        i++;
        } 

        System.out.println("Output Horizontal");
        int o = 10;
        while ( o <= 20 ){
        System.out.print(o + " ");
        o++;
        } 
    }
}
