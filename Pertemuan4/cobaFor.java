public class cobaFor {
    public static void main(String[] args) {

        //menjumlahkan semua angka dari 1 sampai 10

        int jml=0;
        for (int i = 1 ; i<=10 ; i++) {

            //variabel jml ditambah dengan varibel i
            //0 + jumlah semua bilangan dari angka 1 sampai 10
            
            jml+=i;
        }
        System.out.print ("Jumlah Semua Bilangan : " + jml);
    }
}
