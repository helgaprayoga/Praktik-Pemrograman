public class Grade {
    public static void main(String[] args) {
        
        double nilai = 85;

        if(nilai>=80) {
            System.out.println("A");
        }
        else if((nilai>=60) && (nilai<=79)) {
            System.out.println("B");
        }
        else if((nilai>=40) && (nilai<=59)) {
            System.out.println("C");
        }
        


    }
}
